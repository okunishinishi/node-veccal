var veccal = require('veccal');

//------------------------
// add
//------------------------
console.log(veccal.add([1, 2, 3], [2, 4, 8])); //-> [3, 6, 11]

//------------------------
// multiply
//------------------------
console.log(veccal.multiply([1, 2, 3], [2, 4, 8])); //-> [2, 8, 24]

//------------------------
// scale
//------------------------
console.log(veccal.scale([1, 2, 3], 2)); //-> [2, 4, 6]

//------------------------
// size
//------------------------
console.log(veccal.size([3, 4, 5])); //-> 7.0710678118654755

